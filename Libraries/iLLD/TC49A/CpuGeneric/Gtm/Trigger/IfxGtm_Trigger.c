boolean IfxGtm_Trigger_trigToAdc(IfxGtm_Cluster gtmCluster, IfxGtm_TrigSource gtmSource, IfxGtm_TrigChannel Channel, IfxGtm_Cfg_AdcTriggerSignal adcTrigSignal)
{
    boolean                  result   = FALSE;
    uint32                   idx      = 0U;
    IfxGtm_Cfg_TriggerEntry *tablesel = (IfxGtm_Cfg_TriggerEntry *)(void *)IfxGtm_Cfg_AdcTriggerTable[adcTrigSignal];

    if ((IfxGtm_Cfg_AdcTriggerTable[adcTrigSignal] != NULL_PTR) &&
        (tablesel[1].B.cluster == (uint32)gtmCluster))                                                                     /* check if ADC Trigger signal is compatible with cluster */
    {
        for (idx = 1u; idx <= IfxGtm_Cfg_AdcTriggerTable[adcTrigSignal][0]; idx++)                                         /* loop through all SEL values */
        {
            if (IfxGtm_Cfg_AdcTriggerTable[adcTrigSignal][idx] == IFXGTM_CFG_GETTRIGENTRY(gtmCluster, gtmSource, Channel)) /* check for valid combination */
            {
                result = TRUE;
                break;
            }
        }

        if (result == TRUE)
        {
            uint32 shift   = ((uint32)adcTrigSignal & 0x3U) * 8u;
            uint32 trigsel = idx << shift;
            uint32 mask    = (uint32)0x1FU << shift;

            Ifx__ldmst(&(MODULE_GTM.ADC.OUT[gtmCluster].U), mask, trigsel);
        }
    }

    return result;
}


boolean IfxGtm_Trigger_trigToSent(IfxGtm_Cluster gtmCluster, IfxGtm_TrigSource gtmSource, IfxGtm_TrigChannel Channel, IfxGtm_Cfg_SentTriggerModules sentModule, IfxGtm_Cfg_SentTriggerSignal sentTrigSignal)
{
    boolean                  result   = FALSE;
    uint32                   idx      = 0U;
    IfxGtm_Cfg_TriggerEntry *tablesel = (IfxGtm_Cfg_TriggerEntry *)(void *)IfxGtm_Cfg_SentTriggerTable[sentTrigSignal];

    if ((IfxGtm_Cfg_SentTriggerTable[sentTrigSignal] != NULL_PTR) &&
        (tablesel[1].B.cluster == (uint32)gtmCluster))                                                                       /* check if sent Trigger signal is compatible with cluster */
    {
        for (idx = 1u; idx <= IfxGtm_Cfg_SentTriggerTable[sentTrigSignal][0]; idx++)                                         /* loop through all SEL values */
        {
            if (IfxGtm_Cfg_SentTriggerTable[sentTrigSignal][idx] == IFXGTM_CFG_GETTRIGENTRY(gtmCluster, gtmSource, Channel)) /* check for valid combination */
            {
                result = TRUE;
                break;
            }
        }

        if (result == TRUE)
        {
            if ((((uint8)sentTrigSignal - 3) / 6) == 0)
            {
                uint32 shift = ((uint32)sentTrigSignal - 3) * 5;

                if (shift >= 15)
                {
                    ++shift;
                }

                uint32 trigsel = idx << shift;
                uint32 mask    = (uint32)0x1FU << shift;

                Ifx__ldmst(&(MODULE_GTM.SENT[sentModule].OUTA.U), mask, trigsel);
            }
            else
            {
                uint32 shift = ((uint32)sentTrigSignal - (uint32)9u) * (uint32)5u;

                if (shift >= 15)
                {
                    ++shift;
                }

                uint32 trigsel = idx << shift;
                uint32 mask    = (uint32)0x1FU << shift;

                Ifx__ldmst(&(MODULE_GTM.SENT[sentModule].OUTB.U), mask, trigsel);
            }
        }
    }

    return result;
}


