float32 IfxGtm_Cmu_getClkFrequency(Ifx_GTM *gtm, IfxGtm_Cmu_Clk clkIndex, boolean assumeEnabled)
{
    float32 frequency;

    if ((IfxGtm_Cmu_isClkClockEnabled(gtm, clkIndex) != FALSE) || (assumeEnabled != FALSE))
    {
        switch (clkIndex)
        {
        case IfxGtm_Cmu_Clk_0:
            frequency = (float32)(IfxGtm_Cmu_getGclkFrequency(gtm) / ((float32)gtm->CLS->CMU.CLK0.CTRL.B.CLK_CNT + 1.0f));
            break;
        case IfxGtm_Cmu_Clk_1:
            frequency = (float32)(IfxGtm_Cmu_getGclkFrequency(gtm) / ((float32)gtm->CLS->CMU.CLK1.CTRL.B.CLK_CNT + 1.0f));
            break;
        case IfxGtm_Cmu_Clk_2:
            frequency = (float32)(IfxGtm_Cmu_getGclkFrequency(gtm) / ((float32)gtm->CLS->CMU.CLK2.CTRL.B.CLK_CNT + 1.0f));
            break;
        case IfxGtm_Cmu_Clk_3:
            frequency = (float32)(IfxGtm_Cmu_getGclkFrequency(gtm) / ((float32)gtm->CLS->CMU.CLK3.CTRL.B.CLK_CNT + 1.0f));
            break;
        case IfxGtm_Cmu_Clk_4:
            frequency = (float32)(IfxGtm_Cmu_getGclkFrequency(gtm) / ((float32)gtm->CLS->CMU.CLK4.CTRL.B.CLK_CNT + 1.0f));
            break;
        case IfxGtm_Cmu_Clk_5:
            frequency = (float32)(IfxGtm_Cmu_getGclkFrequency(gtm) / ((float32)gtm->CLS->CMU.CLK5.CTRL.B.CLK_CNT + 1.0f));
            break;
        case IfxGtm_Cmu_Clk_6:

            if (gtm->CLS->CMU.CLK_6_CTRL.B.CLK_SEL == 0u)
            {
                frequency = (float32)(IfxGtm_Cmu_getGclkFrequency(gtm) / ((float32)gtm->CLS->CMU.CLK_6_CTRL.B.CLK_CNT + 1.0f));
            }

            else
            {
                /* TODO */
                frequency = 0.0f;
            }

            break;
        case IfxGtm_Cmu_Clk_7:

            if (gtm->CLS->CMU.CLK_7_CTRL.B.CLK_SEL == 0u)
            {
                frequency = (float32)(IfxGtm_Cmu_getGclkFrequency(gtm) / ((float32)gtm->CLS->CMU.CLK_7_CTRL.B.CLK_CNT + 1.0f));
            }

            else
            {
                /* TODO */
                frequency = 0.0f;
            }

            break;
        default:
            frequency = (float32)0.0f;
            break;
        }
    }
    else
    {
        frequency = (float32)0.0f;
    }

    return frequency;
}


float32 IfxGtm_Cmu_getEclkFrequency(Ifx_GTM *gtm, IfxGtm_Cmu_Eclk clkIndex, boolean assumeEnabled)
{
    float32 frequency;

    if ((IfxGtm_Cmu_isEclkClockEnabled(gtm, clkIndex) == TRUE) || (assumeEnabled != FALSE))
    {
        float32 Z = 0.0f;
        float32 N = 0.0f;
        Z = (float32)gtm->CLS->CMU.ECLK[clkIndex].NUM.B.ECLK_NUM;
        N = (float32)gtm->CLS->CMU.ECLK[clkIndex].DEN.B.ECLK_DEN;

        float32 multiplier = N / Z / 2.0f;
        frequency = IfxGtm_Cmu_getGclkFrequency(gtm) * multiplier;
    }
    else
    {
        frequency = (float32)0.0f;
    }

    return frequency;
}


float32 IfxGtm_Cmu_getFxClkFrequency(Ifx_GTM *gtm, IfxGtm_Cmu_Fxclk clkIndex, boolean assumeEnabled)
{
    float32 frequency;
    uint8   fxSelect;

    if ((IfxGtm_Cmu_isFxClockEnabled(gtm) == TRUE) || (assumeEnabled != FALSE))
    {
        fxSelect = gtm->CLS->CMU.FXCLK_CTRL.B.FXCLK_SEL;

        if (fxSelect == 0u)
        {
            frequency = IfxGtm_Cmu_getGclkFrequency(gtm);
        }
        else if (fxSelect <= 6u)
        {
            frequency = IfxGtm_Cmu_getClkFrequency(gtm, (IfxGtm_Cmu_Clk)((uint8)(fxSelect - 1u)), assumeEnabled);
        }
        else if (fxSelect == 7u)
        {
            frequency = IfxGtm_Cmu_getClkFrequency(gtm, IfxGtm_Cmu_Clk_6, assumeEnabled);
        }
        else if (fxSelect == 8u)
        {
            frequency = IfxGtm_Cmu_getClkFrequency(gtm, IfxGtm_Cmu_Clk_7, assumeEnabled);
        }
        else
        {
            frequency = IfxGtm_Cmu_getGclkFrequency(gtm);
        }

        switch (clkIndex)
        {
        case IfxGtm_Cmu_Fxclk_0:
            frequency = frequency / (float32)1.0f;
            break;
        case IfxGtm_Cmu_Fxclk_1:
            frequency = frequency / (float32)16.0f;
            break;
        case IfxGtm_Cmu_Fxclk_2:
            frequency = frequency / (float32)256.0f;
            break;
        case IfxGtm_Cmu_Fxclk_3:
            frequency = frequency / (float32)4096.0f;
            break;
        case IfxGtm_Cmu_Fxclk_4:
            frequency = frequency / (float32)65536.0f;
            break;
        default:
            frequency = (float32)0.0f;
            break;
        }
    }
    else
    {
        frequency = (float32)0.0f;
    }

    return frequency;
}


void IfxGtm_Cmu_setClkFrequency(Ifx_GTM *gtm, IfxGtm_Cmu_Clk clkIndex, float32 frequency)
{
    float32 t   = (IfxGtm_Cmu_getGclkFrequency(gtm) / frequency) - (float32)1.0f;
    uint32  cnt = (uint32)t;

    if ((t - (float32)cnt) > (float32)0.5f)
    {                           /* Round to nearest */
        cnt++;
    }

    switch (clkIndex)
    {
    case IfxGtm_Cmu_Clk_0:
        gtm->CLS->CMU.CLK0.CTRL.B.CLK_CNT = cnt;
        break;
    case IfxGtm_Cmu_Clk_1:
        gtm->CLS->CMU.CLK1.CTRL.B.CLK_CNT = cnt;
        break;
    case IfxGtm_Cmu_Clk_2:
        gtm->CLS->CMU.CLK2.CTRL.B.CLK_CNT = cnt;
        break;
    case IfxGtm_Cmu_Clk_3:
        gtm->CLS->CMU.CLK3.CTRL.B.CLK_CNT = cnt;
        break;
    case IfxGtm_Cmu_Clk_4:
        gtm->CLS->CMU.CLK4.CTRL.B.CLK_CNT = cnt;
        break;
    case IfxGtm_Cmu_Clk_5:
        gtm->CLS->CMU.CLK5.CTRL.B.CLK_CNT = cnt;
        break;
    case IfxGtm_Cmu_Clk_6:
        gtm->CLS->CMU.CLK_6_CTRL.B.CLK_CNT = cnt;
        break;
    case IfxGtm_Cmu_Clk_7:
        gtm->CLS->CMU.CLK_7_CTRL.B.CLK_CNT = cnt;
        break;
    default:
        /* do nothing */
        break;
    }
}


void IfxGtm_Cmu_setEclkFrequency(Ifx_GTM *gtm, IfxGtm_Cmu_Eclk clkIndex, float32 frequency)
{
    float32 f;
    float32 bestDistance = frequency;
    float32 fIn          = IfxGtm_Cmu_getGclkFrequency(gtm) * 2.0f;
    uint32  z, n, nBest = 1u, zBest = 1u;
    float32 t;

    for (z = 1u; z < 0xFFFFFFu; z++)
    {
        boolean endLoop = FALSE;
        t = fIn / z;

        for (n = z; n > 0u; n--)
        {
            float32 distance;
            f        = t * n;
            distance = fabsf(frequency - f);

            if (distance < bestDistance)
            {
                bestDistance = distance;
                nBest        = n;
                zBest        = z;
            }

            if (bestDistance < 0.1f)
            {
                endLoop = TRUE;
                break;
            }
        }

        if (endLoop)
        {
            break;
        }
    }

    gtm->CLS->CMU.ECLK[clkIndex].NUM.B.ECLK_NUM = zBest;
    gtm->CLS->CMU.ECLK[clkIndex].NUM.B.ECLK_NUM = zBest; /* write twice to be sure */
    gtm->CLS->CMU.ECLK[clkIndex].DEN.B.ECLK_DEN = nBest;
}


void IfxGtm_Cmu_setGclkFrequency(Ifx_GTM *gtm, float32 frequency)
{
    float32 f;
    float32 bestDistance = frequency;

    float32 fIn          = IfxGtm_Cmu_getModuleFrequency(gtm);
    uint32  z, n, nBest = 1u, zBest = 1u;
    float32 t;

    for (z = 1u; z < 0xFFFFFFu; z++)
    {
        boolean endLoop = FALSE;
        t = fIn / z;

        for (n = z; n > 0u; n--)
        {
            float32 distance;
            f        = t * n;
            distance = fabsf(frequency - f);

            if (distance < bestDistance)
            {
                bestDistance = distance;
                nBest        = n;
                zBest        = z;
            }

            if (bestDistance < 0.1f)
            {
                endLoop = TRUE;
                break;
            }
        }

        if (endLoop)
        {
            break;
        }
    }

    gtm->CLS->CMU.GCLK_NUM.B.GCLK_NUM = zBest;
    gtm->CLS->CMU.GCLK_NUM.B.GCLK_NUM = zBest;   /* write twice to be sure */
    gtm->CLS->CMU.GCLK_DEN.B.GCLK_DEN = nBest;
}
